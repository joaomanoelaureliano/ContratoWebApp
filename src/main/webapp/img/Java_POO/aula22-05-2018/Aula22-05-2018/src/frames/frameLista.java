    private void buttonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAdicionarActionPerformed
        // TODO add your handling code here:
        if (textNome.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Obrigat√≥rio");
            textNome.requestFocus();
            return;
        }
        lista.add(textNome.getText().toUpperCase());
        textNome.setText("");
    }//GEN-LAST:event_buttonAdicionarActionPerformed

    private void buttonListarTodosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonListarTodosActionPerformed
        // TODO add your handling code here:
        areaTexto.setText("");
        if (lista.isEmpty()){
            areaTexto.append("Lista VAZIA");
            return;
        }
        for (String valor : lista){
            areaTexto.append(valor+"\n");
        }
    }//GEN-LAST:event_buttonListarTodosActionPerformed

