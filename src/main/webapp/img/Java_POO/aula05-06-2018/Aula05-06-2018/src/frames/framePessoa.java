    private void buttonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAdicionarActionPerformed
        // TODO add your handling code here:
        Pessoa pessoa = new Pessoa();
        pessoa.setNome(textNome.getText());
        pessoa.setIdade(Integer.parseInt(textIdade.getText()));
        pessoa.setSexo(buttonMasculino.isSelected()?'M':'F');
        pessoa.setProfissao(textProfissao.getText());
        
        lista.add(pessoa);
        
        textNome.setText("");
        textIdade.setText("");
        buttonMasculino.setSelected(true);
        textProfissao.setText("");
        textNome.requestFocus();
        
    }//GEN-LAST:event_buttonAdicionarActionPerformed

    private void buttonMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonMostrarActionPerformed
        // TODO add your handling code here:
        for (Pessoa pessoa : lista) {
            areaTexto.append(pessoa+"\n");
        }
    }//GEN-LAST:event_buttonMostrarActionPerformed

